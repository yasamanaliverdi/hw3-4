#include <iostream>

using namespace std;
main () {
	
   float x,y;
	char op;
	cout<<"please enter your expression :";
	cin>>x>>op>>y;
   
    switch(op){

      
		case '+'  :
			cout<<"the result is:"<<x+y;
			
			break ; 
			
		case '-'	:
			 cout<<"the result is:"<<x-y;
		
			break ;
			
		case'*'	:
			 cout<<"the result is:"<<x*y;
		
			break ;
			
		case '/' :
			 cout<<"the result is"<<x/y;
			
			break ;
			
	
		
		
			 
			
			
			
}

}
	
	





